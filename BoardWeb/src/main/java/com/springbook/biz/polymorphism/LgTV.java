package com.springbook.biz.polymorphism;

public class LgTV {
	public void turnOn() {
		System.out.println("LgTV --- 전원 켬");
	}
	
	public void turnOff() {
		System.out.println("LgTV --- 전원 끔");
	}
	
	public void soundUp() {
		System.out.println("LgTV --- 소리 키움");
	}
	
	public void soundDown() {
		System.out.println("LgTV --- 소리 낮춤");
	}
}
